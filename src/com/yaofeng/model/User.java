package com.yaofeng.model;

public class User {
	private Integer emp_id;
	private String emp_name;
	private String emp_password;
	private String emp_nickname;
	private String emp_age;
	private String emp_sex;
	private String emp_phone;
	private String emp_address;
	private Integer isAdmin;
	
	public Integer getIsAdmin() {
		return isAdmin;
	}
	public void setIsAdmin(Integer isAdmin) {
		this.isAdmin = isAdmin;
	}
	public Integer getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(Integer emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getEmp_password() {
		return emp_password;
	}
	public void setEmp_password(String emp_password) {
		this.emp_password = emp_password;
	}
	public String getEmp_nickname() {
		return emp_nickname;
	}
	public void setEmp_nickname(String emp_nickname) {
		this.emp_nickname = emp_nickname;
	}
	public String getEmp_age() {
		return emp_age;
	}
	public void setEmp_age(String emp_age) {
		this.emp_age = emp_age;
	}
	public String getEmp_sex() {
		return emp_sex;
	}
	public void setEmp_sex(String emp_sex) {
		this.emp_sex = emp_sex;
	}
	public String getEmp_phone() {
		return emp_phone;
	}
	public void setEmp_phone(String emp_phone) {
		this.emp_phone = emp_phone;
	}
	public String getEmp_address() {
		return emp_address;
	}
	public void setEmp_address(String emp_address) {
		this.emp_address = emp_address;
	}
	
}
